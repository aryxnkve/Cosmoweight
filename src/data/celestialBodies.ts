export function getBodiesByCategory(category: Category): CelestialBody[] {
  return celestialBodies.filter((b) => b.category === category);
}

export function getBodyById(id: string): CelestialBody | undefined {
  return celestialBodies.find((b) => b.id === id);
}

export function searchBodies(query: string): CelestialBody[] {
  const q = query.toLowerCase().trim();
  if (!q) return celestialBodies;
  return celestialBodies.filter((b) => b.name.toLowerCase().includes(q));
}
